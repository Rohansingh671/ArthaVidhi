<!-- JavaScript for Toggle Functionality -->
<script>
    document.getElementById("toggleRowButtonLabour").addEventListener("click", function() {
        var secondRow2 = document.getElementById("secondRow2");
        if (secondRow2.style.display === "none") {
            secondRow2.style.display = "flex"; // Show the row
        } else {
            secondRow2.style.display = "none"; // Hide the row
        }
    });
</script>
