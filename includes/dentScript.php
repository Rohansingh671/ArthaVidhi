<!-- JavaScript for Toggle Functionality -->
<script>
    document.getElementById("toggleRowButtonDent").addEventListener("click", function() {
        var secondRow3 = document.getElementById("secondRow3");
        if (secondRow3.style.display === "none") {
            secondRow3.style.display = "flex"; // Show the row
        } else {
            secondRow3.style.display = "none"; // Hide the row
        }
    });
</script>
