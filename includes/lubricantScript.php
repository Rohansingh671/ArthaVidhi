<!-- JavaScript for Toggle Functionality -->
<script>
    document.getElementById("toggleRowButton").addEventListener("click", function() {
        var secondRow = document.getElementById("secondRow");
        if (secondRow.style.display === "none") {
            secondRow.style.display = "flex"; // Show the row
        } else {
            secondRow.style.display = "none"; // Hide the row
        }
    });
</script>
