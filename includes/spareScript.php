<!-- JavaScript for Toggle Functionality -->
<script>
    document.getElementById("toggleRowButtonSpare").addEventListener("click", function() {
        var secondRow4 = document.getElementById("secondRow4");
        if (secondRow4.style.display === "none") {
            secondRow4.style.display = "flex"; // Show the row
        } else {
            secondRow4.style.display = "none"; // Hide the row
        }
    });
</script>
